package stringPrograms;

public class UpperCaseLowerCaseMethod {

	public static void main(String[] args) {

		// The java string toUpperCase() method converts this string into uppercase
		// letter and string toLowerCase() method into lowercase letter.

		String str1 = "String";
		String str2 = "has";
		String str3 = "POWERFUL";
		String str4 = "mETHODS";
		System.out.println(
				str1.toUpperCase() + " " + str2.toUpperCase() + " " + str3.toLowerCase() + " " + str4.toLowerCase());

	}

}
